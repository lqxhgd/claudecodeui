#!/bin/bash
# =============================================================================
# Claude Code UI - Domain + Free SSL Setup Script
# 域名 + 免费SSL证书配置脚本
# =============================================================================
# Usage: bash setup-domain.sh your-domain.com
# Example: bash setup-domain.sh xcopenclaw.xin

set -e

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
NC='\033[0m'

DOMAIN="$1"

if [ -z "$DOMAIN" ]; then
    echo -e "${RED}Usage: bash setup-domain.sh <your-domain>${NC}"
    echo -e "${CYAN}Example: bash setup-domain.sh xcopenclaw.xin${NC}"
    exit 1
fi

echo -e "${CYAN}"
echo "╔═══════════════════════════════════════════════════╗"
echo "║  Claude Code UI - Domain & SSL Setup              ║"
echo "║  域名和免费SSL证书配置                               ║"
echo "╚═══════════════════════════════════════════════════╝"
echo -e "${NC}"
echo -e "${YELLOW}Domain / 域名: ${CYAN}${DOMAIN}${NC}"
echo ""

# =============================================================================
# Step 1: Install Nginx
# =============================================================================
echo -e "${YELLOW}[1/4] Installing Nginx...${NC}"

if command -v nginx &> /dev/null; then
    echo -e "${GREEN}Nginx already installed: $(nginx -v 2>&1)${NC}"
else
    if command -v apt-get &> /dev/null; then
        apt-get update && apt-get install -y nginx
    elif command -v yum &> /dev/null; then
        yum install -y nginx
    else
        echo -e "${RED}Cannot install Nginx. Please install it manually.${NC}"
        exit 1
    fi
    systemctl enable nginx
    systemctl start nginx
    echo -e "${GREEN}Nginx installed successfully${NC}"
fi

# =============================================================================
# Step 2: Install Certbot (Let's Encrypt)
# =============================================================================
echo -e "${YELLOW}[2/4] Installing Certbot (Let's Encrypt free SSL)...${NC}"

if command -v certbot &> /dev/null; then
    echo -e "${GREEN}Certbot already installed${NC}"
else
    if command -v apt-get &> /dev/null; then
        apt-get install -y certbot python3-certbot-nginx
    elif command -v yum &> /dev/null; then
        yum install -y certbot python3-certbot-nginx
    else
        # Fallback: snap
        snap install --classic certbot 2>/dev/null || {
            echo -e "${RED}Cannot install certbot. Please install it manually.${NC}"
            exit 1
        }
        ln -sf /snap/bin/certbot /usr/bin/certbot
    fi
    echo -e "${GREEN}Certbot installed${NC}"
fi

# =============================================================================
# Step 3: Configure Nginx (HTTP first for cert verification)
# =============================================================================
echo -e "${YELLOW}[3/4] Configuring Nginx...${NC}"

# Create initial HTTP-only config for certificate verification
cat > /etc/nginx/conf.d/claudecodeui.conf << NGINX_CONF
# Claude Code UI - Nginx Reverse Proxy
# Auto-generated by setup-domain.sh

server {
    listen 80;
    server_name ${DOMAIN};

    # Let's Encrypt verification
    location /.well-known/acme-challenge/ {
        root /var/www/certbot;
    }

    location / {
        proxy_pass http://127.0.0.1:3001;
        proxy_http_version 1.1;
        proxy_set_header Upgrade \$http_upgrade;
        proxy_set_header Connection "upgrade";
        proxy_set_header Host \$host;
        proxy_set_header X-Real-IP \$remote_addr;
        proxy_set_header X-Forwarded-For \$proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto \$scheme;
        proxy_read_timeout 86400;
        proxy_send_timeout 86400;

        # WebSocket support
        proxy_buffering off;
        proxy_cache_bypass \$http_upgrade;
    }
}
NGINX_CONF

mkdir -p /var/www/certbot

# Test and reload nginx
nginx -t && systemctl reload nginx
echo -e "${GREEN}Nginx HTTP proxy configured for ${DOMAIN}${NC}"

# Open port 80 and 443 in firewall
if command -v firewall-cmd &> /dev/null; then
    firewall-cmd --permanent --add-service=http 2>/dev/null
    firewall-cmd --permanent --add-service=https 2>/dev/null
    firewall-cmd --reload 2>/dev/null
elif command -v ufw &> /dev/null; then
    ufw allow 'Nginx Full' 2>/dev/null
fi

# =============================================================================
# Step 4: Get SSL Certificate (FREE from Let's Encrypt)
# =============================================================================
echo -e "${YELLOW}[4/4] Getting free SSL certificate from Let's Encrypt...${NC}"
echo -e "${CYAN}SSL证书完全免费！来自 Let's Encrypt${NC}"
echo ""

read -p "Enter your email for SSL certificate notifications (输入邮箱): " SSL_EMAIL

if [ -z "$SSL_EMAIL" ]; then
    SSL_EMAIL="admin@${DOMAIN}"
fi

# Get certificate
certbot --nginx -d "${DOMAIN}" --non-interactive --agree-tos --email "${SSL_EMAIL}" --redirect

# Verify
if [ $? -eq 0 ]; then
    echo -e "${GREEN}SSL certificate obtained successfully!${NC}"

    # Add WebSocket-specific config to the SSL server block
    # Certbot creates the SSL config, we just need to ensure WebSocket headers are there
    if ! grep -q "proxy_read_timeout 86400" /etc/nginx/conf.d/claudecodeui.conf; then
        echo -e "${CYAN}WebSocket proxy config already included${NC}"
    fi

    nginx -t && systemctl reload nginx

    # Set up auto-renewal cron
    if ! crontab -l 2>/dev/null | grep -q "certbot renew"; then
        (crontab -l 2>/dev/null; echo "0 3 * * * certbot renew --quiet && systemctl reload nginx") | crontab -
        echo -e "${GREEN}Auto-renewal configured (daily check at 3 AM)${NC}"
    fi
else
    echo -e "${RED}SSL certificate failed. Make sure:${NC}"
    echo -e "${CYAN}1. Domain DNS A record points to this server's IP${NC}"
    echo -e "${CYAN}2. Port 80 is open in security group${NC}"
    echo -e "${CYAN}3. Domain is registered and active${NC}"
    echo ""
    echo -e "${YELLOW}You can retry later: certbot --nginx -d ${DOMAIN}${NC}"
    echo ""
    echo -e "${GREEN}HTTP access still works: http://${DOMAIN}${NC}"
fi

# =============================================================================
# Summary
# =============================================================================
PUBLIC_IP=$(curl -sf http://ipinfo.io/ip 2>/dev/null || echo "YOUR_IP")

echo ""
echo -e "${GREEN}╔═══════════════════════════════════════════════════════════╗${NC}"
echo -e "${GREEN}║          Domain Setup Complete! 域名配置完成!              ║${NC}"
echo -e "${GREEN}╠═══════════════════════════════════════════════════════════╣${NC}"
echo -e "${GREEN}║                                                           ║${NC}"
echo -e "${GREEN}║  HTTPS: ${CYAN}https://${DOMAIN}${GREEN}                              ║${NC}"
echo -e "${GREEN}║  HTTP:  ${CYAN}http://${DOMAIN}${GREEN}  (auto-redirect to HTTPS)    ║${NC}"
echo -e "${GREEN}║                                                           ║${NC}"
echo -e "${GREEN}║  DingTalk Webhook:                                         ║${NC}"
echo -e "${GREEN}║  ${CYAN}https://${DOMAIN}/api/bot/dingtalk/webhook${GREEN}             ║${NC}"
echo -e "${GREEN}║                                                           ║${NC}"
echo -e "${GREEN}║  WeChat Work Webhook:                                      ║${NC}"
echo -e "${GREEN}║  ${CYAN}https://${DOMAIN}/api/bot/wechat/webhook${GREEN}               ║${NC}"
echo -e "${GREEN}║                                                           ║${NC}"
echo -e "${GREEN}╚═══════════════════════════════════════════════════════════╝${NC}"
echo ""
echo -e "${YELLOW}DNS Setup / DNS配置:${NC}"
echo -e "  在域名管理中添加 A 记录:"
echo -e "  记录类型: A"
echo -e "  主机记录: @"
echo -e "  记录值: ${PUBLIC_IP}"
echo ""
echo -e "${YELLOW}Commands / 管理命令:${NC}"
echo -e "  Check SSL:     certbot certificates"
echo -e "  Renew SSL:     certbot renew"
echo -e "  Nginx logs:    tail -f /var/log/nginx/error.log"
echo -e "  Nginx reload:  systemctl reload nginx"
echo ""
